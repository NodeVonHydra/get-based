import { saveImportedData } from './data.js';

export async function handleDNAFile(file) {
  try {
    showNotification('Parsing DNA file...', 'info');
    const result = await parseDNAFile(file);
    if (Object.keys(result.matches).length === 0) {
      showNotification('No health-relevant SNPs found in this file. Is it a DNA raw data export?', 'error');
      return;
    }
    showDNAImportPreview(result, file.name);
  } catch (e) {
    console.error('DNA import error:', e);
    showNotification(e.message || 'Failed to parse DNA file', 'error');
  }
}

function showDNAImportPreview(result, fileName) {
  window._pendingDNAImport = result;

  // Categorize matches by effect — skip raw APOE components when haplotype resolved
  const apoe = resolveAPOE(result.matches);
  const apoeRsids = apoe ? new Set(['rs429358', 'rs7412']) : new Set();
  const significant = [], moderate = [], none = [], unknown = [];
  for (const [rsid, m] of Object.entries(result.matches)) {
    if (apoeRsids.has(rsid)) continue;
    const item = { rsid, ...m };
    if (m.effect === 'significant') significant.push(item);
    else if (m.effect === 'moderate') moderate.push(item);
    else if (m.effect === 'none') none.push(item);
    else unknown.push(item);
  }

  const effectIcon = { significant: '\uD83D\uDD34', moderate: '\uD83D\uDFE1', none: '\uD83D\uDFE2', unknown: '\u2753' };
  const effectLabel = { significant: 'Significant impact', moderate: 'Moderate impact', none: 'Normal / no impact', unknown: 'Not in lookup' };

  function renderGroup(items, label) {
    if (items.length === 0) return '';
    return `<div class="dna-preview-group">
      <div class="dna-preview-group-title">${label} (${items.length})</div>
      ${items.map(m => `<div class="dna-preview-row">
        <span class="dna-preview-icon">${effectIcon[m.effect] || '\u2753'}</span>
        <span class="dna-preview-gene">${escapeHTML(m.gene)} ${escapeHTML(m.variant)}</span>
        <span class="dna-preview-genotype">${escapeHTML(m.genotype)}</span>
        <span class="dna-preview-note">${escapeHTML(m.note)}</span>
      </div>`).join('')}
    </div>`;
  }

  const html = `
    <div class="dna-preview-header">
      <div class="dna-preview-title">DNA Import — ${escapeHTML(result.source)}</div>
      <div class="dna-preview-stats">${result.totalLines.toLocaleString()} SNPs scanned · ${result.coverage.found} of ${result.coverage.total} health-relevant SNPs found</div>
      ${apoe ? `<div class="dna-preview-apoe">APOE Haplotype: <strong>${escapeHTML(apoe)}</strong></div>` : ''}
    </div>
    <div class="dna-preview-body">
      ${renderGroup(significant, '\uD83D\uDD34 Significant findings')}
      ${renderGroup(moderate, '\uD83D\uDFE1 Moderate findings')}
      ${renderGroup(none, '\uD83D\uDFE2 Normal')}
      ${renderGroup(unknown, '\u2753 Genotype not in lookup')}
    </div>
    <div class="dna-preview-disclaimer">
      Your DNA file was processed locally and never transmitted. Only matched health-relevant SNPs are stored.
      Genetic information is for educational context alongside your lab results — not a medical diagnosis.
    </div>
    <div class="dna-preview-actions">
      <button class="import-btn import-btn-secondary" onclick="closeDNAImportPreview()">Cancel</button>
      <button class="import-btn import-btn-primary" onclick="confirmDNAImport()">Import ${result.coverage.found} SNPs</button>
    </div>`;

  // Use the existing import modal overlay
  let overlay = document.getElementById('import-modal-overlay');
  if (!overlay) {
    overlay = document.createElement('div');
    overlay.id = 'import-modal-overlay';
    overlay.className = 'modal-overlay';
    document.body.appendChild(overlay);
  }
  overlay.innerHTML = `<div class="modal dna-preview-modal" role="dialog">${html}</div>`;
  overlay.classList.add('show');
}

function closeDNAImportPreview() {
  window._pendingDNAImport = null;
  const overlay = document.getElementById('import-modal-overlay');
  if (overlay) overlay.classList.remove('show');
}

function confirmDNAImport() {
  const result = window._pendingDNAImport;
  if (!result) return;
  saveGeneticsData(state.importedData, result);
  saveImportedData();
  window._pendingDNAImport = null;
  const overlay = document.getElementById('import-modal-overlay');
  if (overlay) overlay.classList.remove('show');
  showNotification(`Imported ${result.coverage.found} SNPs from ${result.source}`, 'success');
  // Refresh dashboard
  if (window.navigate) window.navigate('dashboard');
}

