// Detect DNA file by checking first few lines of text content
export function detectDNAFile(text) {
  const first = text.slice(0, 500);
  if (first.startsWith('#AncestryDNA')) return 'ancestry';
  if (first.includes('# This data file generated by 23andMe')) return '23andme';
  if (first.includes('# Living DNA customer genotype data download file version')) return 'livingdna';
  // CSV formats — no comment lines, start with header
  const firstLine = first.split(/\r?\n/)[0].trim();
  if (/^RSID,CHROMOSOME,POSITION,RESULT$/i.test(firstLine)) return 'csv'; // MyHeritage or FTDNA
  if (/^"?RSID"?,/i.test(firstLine)) return 'csv'; // quoted variant
  return null;
}

// Check if a dropped/selected file looks like a DNA raw data file
export function isDNAFile(file) {
  if (!file) return false;
  const name = file.name.toLowerCase();
  // Common DNA file patterns
  if (name.includes('ancestrydna')) return true;
  if (name.includes('23andme')) return true;
  if (name.includes('myheritage') && name.includes('dna')) return true;
  if (name.includes('family_finder')) return true;
  if (name.includes('livingdna') || name.includes('living_dna')) return true;
  return false;
}

// ═══════════════════════════════════════════════
// WEB WORKER PARSER (inline blob)
// ═══════════════════════════════════════════════

const WORKER_CODE = `
// DNA parser worker — receives { file, snpIds, format }
// Posts back { matches, source, totalLines, format }

self.onmessage = async function(e) {
  const { file, snpIds, format } = e.data;
  const text = await file.text();
  const lines = text.split(/\\r?\\n/);
  const lookupSet = new Set(snpIds);
  const matches = {};
  let totalData = 0;
  let detectedFormat = format;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    if (!line || line.startsWith('#')) continue;

    // Skip header lines
    const upper = line.toUpperCase();
    if (upper.startsWith('RSID') || upper.startsWith('"RSID')) continue;

    let rsid, genotype;

    if (detectedFormat === 'ancestry') {
      // Tab-separated: rsid, chromosome, position, allele1, allele2
      const parts = line.split('\\t');
      if (parts.length < 5) continue;
      rsid = parts[0].trim();
      const a1 = parts[3].trim();
      const a2 = parts[4].trim();
      if (a1 === '0' || a2 === '0' || a1 === '-' || a2 === '-') { totalData++; continue; }
      genotype = a1 + a2;
    } else if (detectedFormat === '23andme' || detectedFormat === 'livingdna') {
      // Tab-separated: rsid, chromosome, position, genotype
      const parts = line.split('\\t');
      if (parts.length < 4) continue;
      rsid = parts[0].trim();
      genotype = parts[3].trim();
      if (genotype === '--' || genotype === '00') { totalData++; continue; }
    } else if (detectedFormat === 'csv') {
      // Comma-separated: RSID, CHROMOSOME, POSITION, RESULT (possibly quoted)
      const parts = line.split(',').map(s => s.replace(/^"|"$/g, '').trim());
      if (parts.length < 4) continue;
      rsid = parts[0];
      genotype = parts[3];
      if (genotype === '--' || genotype === '00' || !genotype) { totalData++; continue; }
    } else {
      continue;
    }

    // Skip internal IDs (23andMe uses i-prefixed IDs for some markers)
    if (!rsid.startsWith('rs')) { totalData++; continue; }

    totalData++;

    if (lookupSet.has(rsid)) {
      matches[rsid] = genotype;
    }
  }

  self.postMessage({ matches, source: detectedFormat, totalLines: totalData, format: detectedFormat });
};
`;

let _worker = null;

function getWorker() {
  if (!_worker) {
    const blob = new Blob([WORKER_CODE], { type: 'application/javascript' });
    _worker = new Worker(URL.createObjectURL(blob));
  }
  return _worker;
}

